fun main() {
    var customers = 10
    println(customers)

    customers = 8
    println(customers)

    customers = customers + 3
    println(customers)

    customers += 7
    println(customers)

    customers -= 3
    println(customers)

    customers *= 2
    println(customers)

    customers /= 3
    println(customers)

    val d: Int
    d = 3

    val e: String = "hello"

    println(d)
    println(e)

    val aa = 1000
    val bb = "log message"
    val cc = 3.14
    val dd = 100_000_000_000_000
    val ee = false
    val ff = '\n'

    /*
        val aa: Int = 1000
